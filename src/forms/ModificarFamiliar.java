    public void escribirDatos(){
   
        Familiar fam = cp.getFamiliar(id);
        txtNombre.setText(fam.getNombre());
        txtApellidoPa.setText(fam.getApellidoPa());
        txtApellidoMa.setText(fam.getApellidoMa());
        txtTelefono1.setText(fam.getTelefono1());
        txtTelefono2.setText(fam.getTelefono2());
        txtDireccion.setText(fam.getDireccion());
        txtCp.setText(fam.getCp());
        txtCorreoE.setText(fam.getCorreoE());
        dateChooser.setDate(fam.getFechaNac());
        if("Hombre".equals(fam.getSexo())){
            sexo.setSelectedIndex(0);
        }else if("Mujer".equals(fam.getSexo())){
            sexo.setSelectedIndex(1);
        }else{
            sexo.setSelectedIndex(0);
        }
    }
    
    
    
    
    
    
