    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        Familiar familiar = new Familiar(0);
        
        familiar.setIdFamilar(id);
        familiar.setNombre(txtNombre.getText());
        familiar.setApellidoPa(txtApellidoPa.getText());
        familiar.setApellidoMa(txtApellidoMa.getText());
        familiar.setTelefono1(txtTelefono1.getText());
        familiar.setTelefono2(txtTelefono2.getText());
        familiar.setDireccion(txtDireccion.getText());
        familiar.setCp(txtCp.getText());
        familiar.setCorreoE(txtCorreoE.getText());
        familiar.setSexo(sexo.getSelectedItem().toString());
        familiar.setFechaNac(dateChooser.getDate());
  
        try {
            cp.modificarFamiliar(familiar);
        } catch (Exception ex) {
            Logger.getLogger(ModificarUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        formUs.actualizarTabla();
        JOptionPane.showMessageDialog(null, "Se modifico usuario exitosamente");
        this.dispose();
    }//GEN-LAST:event_btnOkActionPerformed

